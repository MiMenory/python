# Задача 30:  Заполните массив элементами арифметической прогрессии. Её первый элемент, разность и количество элементов нужно ввести с клавиатуры. 
# Формула для получения n-го члена прогрессии: an = a1 + (n-1) * d.
# Каждое число вводится с новой строки.

# a1 - первый член прогрессии
# d - разность прогрессии
# n - номер челна


a1 = int(input('Введите первый элемент: '))
d = int(input('Разность: '))
n = int(input('Номер члена: '))
array = []

def arithmetic_progression(a1, d, n):
    for i in range(1, n+1):
        summa = a1 + (i - 1) * d
        array.append(summa)
    return array


print(arithmetic_progression(a1, d, n))



